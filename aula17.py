# If, elif, else

condicao_1 = False
condicao_2 = False
condicao_3 = True
condicao_4 = False

if condicao_1:
    print('Codigo da condição 1')
elif condicao_2:
    print('Codigo da condição 2')
elif condicao_3:
    print('Codigo da condição 3')
elif condicao_4:
    print('Codigo da condição 4')
else:
    print('Ultima condição..')
    
if 10 == 10:
    print('Segundo if..')

print('Fora de todos os ifs')
