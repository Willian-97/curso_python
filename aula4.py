# Temos dois tipos de numeros em python float e int

# int => numeros inteiros positivos ou negativos
print(1)
print(-5)

# float => numeros com ponto flutuante (casas decimais) positivos ou negativos
print(1.5)
print(-2.0)

# A função/class type() serve para verificar o tipo de uma var
print(type(1), type(-5))
print(type(1.5), type(-2.0))
print(type('Willian'))