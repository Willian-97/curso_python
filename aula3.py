'''
str => string => texto
Python é uma linguagem que combina tipagem dinamica e forte
'''

# tipo inteiro
print('1234')

# Tipo String
# Aspas simples
print('Willian')
print(1,'Willian "gasparini"')
# Aspas duplas
print("Willian Gasparini")
print(2, "Willian 'Gasparini'")

# Não usados 
# escape \
print("Willian \"Gasparini\"")

# r
print(r"Willian \"Gasparini\"")
