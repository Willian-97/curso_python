# Coerção de tipos, conversão de tipos ou typecasting 
# podemos mudar o tipo de um dado, usando a coerção 

# resulta em erro pois a soma não é possivel com string e int

# print('1' + 1) 
print(type('1'))

# convertendo str em int
print(int('2') + 5)
print('2', type(int('2')))

# convetendo int em float
print(float(1) + 8)
print(type(float(1)))

# convertendo em str
print('x = ' + str(15))
print(type(str(15)))